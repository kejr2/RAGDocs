"""
Pytest session fixtures for the RAGDocs eval harness.
Writes eval_report.md at the end of the test session.
"""
import json
import time
from pathlib import Path
from datetime import datetime, timezone

import pytest

# Shared store: list of result dicts filled in by test_eval_battery.py
_results: list = []


def register_result(result: dict):
    """Called by each parametrized test to record its outcome."""
    _results.append(result)


@pytest.fixture(scope="session", autouse=True)
def write_eval_report():
    """Session-scoped fixture — yields (tests run), then writes the markdown report."""
    yield  # let all tests run

    if not _results:
        return

    report_path = Path(__file__).parent / "eval_report.md"
    now_iso = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")

    passed  = sum(1 for r in _results if r["verdict"] == "PASS")
    hedged  = sum(1 for r in _results if r["verdict"] == "HEDGED")
    failed  = sum(1 for r in _results if r["verdict"] == "FAIL")
    total   = len(_results)
    pct     = round(passed / total * 100) if total else 0

    verdict_icon = {"PASS": "✅ PASS", "HEDGED": "🟡 HEDGED", "FAIL": "❌ FAIL"}

    rows = "\n".join(
        f"| {r['id']} | {r['question'][:55]}… | {verdict_icon.get(r['verdict'], r['verdict'])} "
        f"| {r['confidence']} | {r['sources']} | {r['latency_ms']}ms |"
        for r in sorted(_results, key=lambda x: x["id"])
    )

    report = f"""# RAGDocs Eval Report
_Run at {now_iso}_

**Accuracy: {passed}/{total} ({pct}%)**

| PASS | HEDGED | FAIL |
|------|--------|------|
| {passed:4d} | {hedged:6d} | {failed:4d} |

## Results

| ID | Question | Verdict | Confidence | Sources | Latency |
|----|----------|---------|------------|---------|---------|
{rows}

---
_Generated by `tests/eval/test_eval_battery.py`_
"""
    report_path.write_text(report, encoding="utf-8")
    print(f"\n{'='*60}")
    print(f"  Eval report: {report_path}")
    print(f"  Accuracy:    {passed}/{total} ({pct}%)")
    print(f"{'='*60}")
